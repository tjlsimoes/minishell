#include "libft.h"

int	ft_isspace(int c)
{
	return (c == ' ' || c == '\t' || c == '\n'
		|| c == '\v' || c == '\f' || c == '\r');
}

// ' ': space
// '\t': horizontal tab
// '\n': newline
// '\v': vertical tab
// '\f': form feed
// '\r': carriage return
